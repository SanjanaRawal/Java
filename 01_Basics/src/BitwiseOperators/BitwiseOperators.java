package BitwiseOperators;

public class BitwiseOperators {
    public static void main(String[] args) {
        int a=10 , b=8 ;
        System.out.print("a&b = ");
        System.out.println(a&b);
        System.out.print("a|b = ");
        System.out.println(a|b);
        System.out.print("a^b = ");
        System.out.println(a^b);
        System.out.print("~a = ");
        System.out.println(~a);
    }
}
